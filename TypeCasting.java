package typeCasting;

public class TypeCasting {

	public static void main(String[] args) {
		byte x=4;
		int y=x;
		System.out.println(y);     //Automatic Type casting
		
		
		double myDouble=2.8245;
		int myInt = (int)myDouble;
		
		System.out.println(myDouble);
		System.out.println(myInt);

	}

}
